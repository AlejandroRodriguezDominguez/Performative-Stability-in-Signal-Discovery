    "# %% CELL 6 -- EXP C: REVIVAL / ciclo (unico del paper), sobre predictores\n",
    "def revival_test(df, label):\n",
    "    post_df = df[df.regime=='post_pub']\n",
    "    rows=[]\n",
    "    for s, sub in post_df.groupby('signal'):\n",
    "        sub=sub.sort_values('date')\n",
    "        if len(sub)<72: continue\n",
    "        e=sub['ret_dec'].rolling(36).mean().dropna().values\n",
    "        if len(e)<24: continue\n",
    "        med=np.median(e); lo,hi=[],[]\n",
    "        for t in range(len(e)-12):\n",
    "            (lo if e[t]<med else hi).append(e[t+12])\n",
    "        if lo and hi: rows.append((s,np.mean(lo),np.mean(hi)))\n",
    "    rev=pd.DataFrame(rows,columns=['signal','after_crowded','after_slack'])\n",
    "    tt=st.ttest_rel(rev['after_crowded'],rev['after_slack'])\n",
    "    fr=(rev.after_crowded>rev.after_slack).mean()\n",
    "    print(f'[{label}] revival n={len(rev)}  after_crowded={rev.after_crowded.mean()*100:.3f}%  '\n",
    "          f'after_slack={rev.after_slack.mean()*100:.3f}%  t={tt.statistic:.2f} p={tt.pvalue:.1e}  frac={fr*100:.0f}%')\n",
    "    return dict(label=label,n=len(rev),ac=rev.after_crowded.mean(),as_=rev.after_slack.mean(),\n",
    "                t=tt.statistic,p=tt.pvalue,frac=fr,rev=rev)\n",
    "C = revival_test(dfA,'PRED')\n",
    "Cpl = revival_test(dfB,'PLACEBO')   # el placebo tampoco debe revivir\n"
