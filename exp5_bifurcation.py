    "# %% CELL 2 -- imports + helpers\n",
    "import numpy as np, pandas as pd, scipy.stats as st\n",
    "import zipfile, io\n",
    "import matplotlib.pyplot as plt\n",
    "from matplotlib import rcParams\n",
    "rcParams.update({'font.family':'serif','font.size':9,'axes.linewidth':0.6,\n",
    "                 'axes.spines.top':False,'axes.spines.right':False,\n",
    "                 'figure.dpi':140,'savefig.dpi':200})\n",
    "np.random.seed(0)\n",
    "\n",
    "def parse_ym(x):\n",
    "    s = str(x)\n",
    "    if 'm' in s:\n",
    "        y,m = s.split('m'); return pd.Timestamp(int(y), int(m), 1)\n",
    "    return pd.to_datetime(s, errors='coerce')\n",
    "\n",
    "def normalize_long(df):\n",
    "    \"\"\"Lleva cualquier tabla de retornos a columnas: date, signal, ret (largo).\"\"\"\n",
    "    cols = {c.lower():c for c in df.columns}\n",
    "    date_c = cols.get('date', df.columns[0])\n",
    "    if 'signalname' in cols and 'ret' in cols:       # ya es largo, una pata = LS\n",
    "        out = df.rename(columns={cols['signalname']:'signal', cols['ret']:'ret', date_c:'date'})\n",
    "        return out[['date','signal','ret']].copy()\n",
    "    # formato ancho: date + una columna por senal\n",
    "    wide = df.rename(columns={date_c:'date'})\n",
    "    return wide.melt(id_vars='date', var_name='signal', value_name='ret')\n",
    "\n",
    "def long_short_from_portsfull(df):\n",
    "    \"\"\"De PredictorPortsFull (date,signalname,port,ret) saca el spread LS por senal:\n",
    "       port mas alto menos port mas bajo (las dos puntas de la ordenacion).\"\"\"\n",
    "    cols = {c.lower():c for c in df.columns}\n",
    "    d = df.rename(columns={cols.get('signalname','signalname'):'signal',\n",
    "                           cols.get('port','port'):'port',\n",
    "                           cols.get('ret','ret'):'ret',\n",
    "                           cols.get('date','date'):'date'})\n",
    "    d = d[['date','signal','port','ret']].dropna(subset=['ret'])\n",
    "    # 'port' puede ser numerico (1..N) o etiquetas tipo '01','10','LS'. Si hay 'LS', usalo.\n",
    "    def spread(group):\n",
    "        ports = group['port'].astype(str)\n",
    "        if (ports=='LS').any():\n",
    "            return group.loc[ports=='LS','ret']\n",
    "        # si no, top - bottom por orden de 'port'\n",
    "        g = group.copy(); g['pnum'] = pd.to_numeric(g['port'], errors='coerce')\n",
    "        g = g.dropna(subset=['pnum'])\n",
    "        if g.empty: return pd.Series(dtype=float)\n",
    "        lo, hi = g.pnum.min(), g.pnum.max()\n",
    "        top = g[g.pnum==hi].set_index('date')['ret']\n",
    "        bot = g[g.pnum==lo].set_index('date')['ret']\n",
    "        return (top - bot)\n",
    "    # construir LS por (signal,date)\n",
    "    recs=[]\n",
    "    for sig, grp in d.groupby('signal'):\n",
    "        ls = spread(grp.assign(signal=sig))\n",
    "        if len(ls):\n",
    "            tmp = ls.reset_index(); tmp.columns=['date','ret']; tmp['signal']=sig\n",
    "            recs.append(tmp)\n",
    "    out = pd.concat(recs, ignore_index=True) if recs else pd.DataFrame(columns=['date','signal','ret'])\n",
    "    return out\n",
    "\n",
    "def read_zip_returns(path):\n",
    "    \"\"\"Abre un .zip de AltPorts y concatena todos los CSV de retornos que contenga,\n",
    "       devolviendo LS por senal. Maneja subcarpetas (Original_Cuts, etc.).\"\"\"\n",
    "    frames=[]\n",
    "    with zipfile.ZipFile(path) as z:\n",
    "        csvs = [n for n in z.namelist() if n.lower().endswith('.csv')]\n",
    "        # preferir la carpeta Original_Cuts (sin VW) si existe, para comparar manzanas con manzanas\n",
    "        pref = [n for n in csvs if 'original_cuts' in n.lower() and 'vw' not in n.lower()]\n",
    "        use = pref if pref else csvs\n",
    "        for n in use:\n",
    "            with z.open(n) as fh:\n",
    "                try:\n",
    "                    df = pd.read_csv(fh)\n",
    "                except Exception:\n",
    "                    continue\n",
    "            if 'port' in [c.lower() for c in df.columns]:\n",
    "                ls = long_short_from_portsfull(df)\n",
    "            else:\n",
    "                ls = normalize_long(df)\n",
    "            if len(ls): frames.append(ls)\n",
    "    if not frames: return pd.DataFrame(columns=['date','signal','ret'])\n",
    "    out = pd.concat(frames, ignore_index=True)\n",
    "    out = out.groupby(['date','signal'], as_index=False)['ret'].mean()  # dedup si varias cuts\n",
    "    return out\n"
