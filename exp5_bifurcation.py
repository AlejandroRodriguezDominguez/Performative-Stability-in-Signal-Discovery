    "# %% CELL 3 -- metadatos + funcion de regimenes\n",
    "doc = pd.read_csv(os.path.join(DATA_DIR, 'SignalDoc.csv'))\n",
    "doc = doc[doc['Cat.Signal'] == 'Predictor'].copy()\n",
    "doc['Acronym'] = doc['Acronym'].str.strip()\n",
    "meta = doc[['Acronym','Year','SampleEndYear']].dropna()\n",
    "meta = meta.rename(columns={'Acronym':'signal','Year':'PubYear','SampleEndYear':'SampleEnd'})\n",
    "meta['PubYear']=meta.PubYear.astype(int); meta['SampleEnd']=meta.SampleEnd.astype(int)\n",
    "print(f'{len(meta)} predictores con anyo de publicacion y fin de muestra')\n",
    "\n",
    "def build_regimes(long, meta):\n",
    "    long = long.dropna(subset=['ret']).copy()\n",
    "    long['date'] = long['date'].map(parse_ym)\n",
    "    long['signal'] = long['signal'].str.strip()\n",
    "    long = long.dropna(subset=['date'])\n",
    "    long['year'] = long.date.dt.year\n",
    "    df = long.merge(meta, on='signal', how='inner').sort_values(['signal','date'])\n",
    "    def reg(r):\n",
    "        if r.year <= r.SampleEnd: return 'in_sample'\n",
    "        if r.year <= r.PubYear:   return 'post_sample'\n",
    "        return 'post_pub'\n",
    "    df['regime'] = df.apply(reg, axis=1)\n",
    "    scale = 1.0 if df.ret.abs().median() < 0.05 else 0.01\n",
    "    df['ret_dec'] = df.ret * scale\n",
    "    return df\n",
    "\n",
    "def decay_stats(df, label):\n",
    "    g = df.groupby(['signal','regime'])['ret_dec'].mean().reset_index()\n",
    "    piv = g.pivot(index='signal', columns='regime', values='ret_dec').dropna(subset=['in_sample','post_pub'])\n",
    "    ins, post = piv['in_sample'].values, piv['post_pub'].values\n",
    "    abs_decay = ins.mean() - post.mean()                 # cambio absoluto (robusto)\n",
    "    # fraccion solo si el edge in-sample medio es claramente positivo\n",
    "    frac = abs_decay/ins.mean() if ins.mean() > 0.001 else np.nan\n",
    "    tt = st.ttest_rel(ins, post)\n",
    "    fr_txt = f'{frac*100:.1f}%' if not np.isnan(frac) else 'n/a'\n",
    "    print(f'[{label}] n={len(piv)}  in={ins.mean()*100:.3f}%  post={post.mean()*100:.3f}%  '\n",
    "          f'abs_decay={abs_decay*100:.3f}pp  frac={fr_txt}  t={tt.statistic:.2f} p={tt.pvalue:.1e}')\n",
    "    return dict(label=label, n=len(piv), edge_in=ins.mean(), edge_post=post.mean(),\n",
    "                abs_decay=abs_decay, decay_frac=frac, t=tt.statistic, p=tt.pvalue, piv=piv)\n"
